package com.example.android.persistence.codelab.realmdb;

import io.realm.RealmList;
import io.realm.RealmModel;
import io.realm.annotations.PrimaryKey;
import io.realm.annotations.RealmClass;

@RealmClass
public class User implements RealmModel {

    private @PrimaryKey
    String id;
    private String name;
    private String lastName;
    private int age;
    private RealmList<Loan> loans;

    public User() {}

    public User(String id, String name, String lastName, int age) {
        this.id = id;
        this.name = name;
        this.lastName = lastName;
        this.age = age;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public RealmList<Loan> getLoans() {
        return loans;
    }

    public void setLoans(RealmList<Loan> loans) {
        this.loans = loans;
    }
}
